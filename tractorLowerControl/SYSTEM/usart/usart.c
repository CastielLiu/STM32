extern float g_requestAngle;
u8 requestAngleBuf[40];

void USART2_IRQHandler(void)                 
{
	static u8 i=0;
	u8 checkNum = 0x00;
	if(USART_GetITStatus(USART2, USART_IT_RXNE) != RESET)
	{
		requestAngleBuf[i] = USART_ReceiveData(USART2);
		if(i>=3 && requestAngleBuf[i]==0xff)
		{
			checkNum = requestAngleBuf[i-1];
			if(requestAngleBuf[i-2] + requestAngleBuf[i-3] == checkNum)
			{
				g_requestAngle = *(uint16_t *)(&requestAngleBuf[i-3])*0.01 -180.0;
			}
		}			
		
		i++;
		if(i==40) i=0;
	}
	USART_ClearITPendingBit(USART2,USART_IT_RXNE);//清除中断标志位
} 
 

