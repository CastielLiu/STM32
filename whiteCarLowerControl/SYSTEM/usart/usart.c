u8 data_check(const u8 * buf,u8 len)
{
	u8 temp=0,i=0;
	for(;i<len;i++)
	{
		temp ^= *(buf+i);
	}
	return temp;	
}
void msg_parse(u8 msgID,const u8 * buf)
{
	if(msgID == CAR_CONTROL_ID)
	{
		extern carControlData_t	g_carControlMsg;
		g_carControlMsg.speed_l = buf[0]*256 + buf[1];
		g_carControlMsg.angle = buf[2] *256 + buf[3];
	}
		
}


static u8 usart2_state=0;
u8 msgBuf[10]={0};
void USART2_IRQHandler(void)                 
{
	u8 temp, length_of_data,msgID;
	static u8 i=0;
	if(USART_GetITStatus(USART2, USART_IT_RXNE) != RESET)
	{
		temp = USART_ReceiveData(USART2);
		switch(usart2_state)
		{
			case 0:
				if(temp == 0x55)
					usart2_state = 1;
				break;
			case 1:
				if(temp == 0xAA)
					usart2_state = 2;
				break;
			case 2:
				msgID = temp;
				usart2_state = 3;
				break;
			case 3:
				length_of_data = temp;
				usart2_state = 4;
				break;
			case 4:
				msgBuf[i] = temp;
				i++;
				if(i == length_of_data)
					usart2_state = 5;
				break;
			case 5:
				if(data_check(msgBuf,length_of_data) == temp)
					msg_parse(msgID,msgBuf);
				usart2_state = 0;
				break;
			default:
				usart2_state = 0;
				break;
		}
	}
	USART_ClearITPendingBit(USART2,USART_IT_RXNE);//清除中断标志位
} 




