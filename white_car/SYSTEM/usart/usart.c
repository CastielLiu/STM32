void USART2_IRQHandler(void)                 
{
	if(USART_GetITStatus(USART2, USART_IT_RXNE) != RESET)
	{
		;
	}
	USART_ClearITPendingBit(USART2,USART_IT_RXNE);//清除中断标志位
} 

